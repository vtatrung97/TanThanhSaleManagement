using Microsoft.Extensions.DependencyInjection;
using Delta.SaleProject.HealthChecks;

namespace Delta.SaleProject.Web.HealthCheck
{
    public static class AbpZeroHealthCheck
    {
        public static IHealthChecksBuilder AddAbpZeroHealthCheck(this IServiceCollection services)
        {
            var builder = services.AddHealthChecks();
            builder.AddCheck<SaleProjectDbContextHealthCheck>("Database Connection");
            builder.AddCheck<SaleProjectDbContextUsersHealthCheck>("Database Connection with user check");
            builder.AddCheck<CacheHealthCheck>("Cache");

            // add your custom health checks here
            // builder.AddCheck<MyCustomHealthCheck>("my health check");

            return builder;
        }
    }
}
